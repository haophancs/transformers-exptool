def normalize_token(token, keep_emojis=True, username="@USER", httpurl="httpurl") -> str:
    lowercased_token = token.lower()
    if token.startswith("@"):
        return username
    elif lowercased_token.startswith("http") or lowercased_token.startswith("www"):
        return httpurl
    elif len(token) == 1:
        if keep_emojis:
            demojized = demojize(token)
            if ":regional_indicator_symbol_letter_" in demojized:
                return ""
            if ":globe" in demojized:
                return ":globe:"
            return demojized
        if emoji_count(token) > 0:
            return ""
        return token
    else:
        if token == "’":
            return "'"
        elif token == "…":
            return "..."
        elif token == "ー":
            return "-"
        else:
            return token


